/**
 * @brief      Load supervisor trap entry in RISC-V
 */
void idt_init(void) {
    extern void __alltraps(void);
    /* Set sscratch register to 0, indicating to exception vector that we are
     * presently executing in the kernel */
    //约定：若中断前处于S态，sscratch为0
    //若中断前处于U态，sscratch存储内核栈地址
    //那么之后就可以通过sscratch的数值判断是内核态产生的中断还是用户态产生的中断
    //我们现在是内核态所以给sscratch置零
    write_csr(sscratch, 0);
    //保证__alltraps的地址是四字节对齐的，将__alltraps这个符号的地址直接写到stvec寄存器，设置中断向量表
    /*stvec(Supervisor Trap Vector Base Address Register)，即所谓的”中断向量表基址”，
    作用就是把不同种类的中断映射到对应的中断处理程序。如果只有一个中断处理程序，那么可以让
    stvec 直接指向那个中断处理程序的地址。*/
    //此处即是只有一个中断处理程序的情况
    write_csr(stvec, &__alltraps);
}

/* trap_in_kernel - test if trap happened in kernel */
bool trap_in_kernel(struct trapframe *tf) {
    return (tf->status & SSTATUS_SPP) != 0;
}

